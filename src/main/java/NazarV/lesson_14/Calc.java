package NazarV.lesson_14;

public class Calc {
    public int add(int x, int y) {
        int sum = 0;
        sum = x + y;
        return sum;
    }

    public int sub(int x, int y) {
        int summ = 0;
        summ = x - y;
        return summ;
    }

    public int multi(int x, int y) {
        int sum = 0;
        sum = x * y;
        return sum;
    }

    public int div (int x, int y) {
        int sum = 0;
        sum = x / y;
        return sum;
    }
}